#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	if (s1 && s2)
	{
		while (*s1 && *s2 && i < n)
		{
			if (*s1 != *s2)
				break ;
			s1++;
			s2++;
			i++;
		}
		if (*(s1 - 1) == *(s2 - 1) && i == n)
			return (1);
		if (*s1 != *s2)
			return (0);
		return (1);
	}
	return (0);
}
